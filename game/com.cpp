#include "com.h"

//*****************************************************************************
// 関数定義
//*****************************************************************************
//-------------------------------------
//COM初期化
//引数：	無
//戻値：	BOOL			…	成功時TRUE、エラー時FALSE
//-------------------------------------
BOOL	InitCOM( void )
{
	HRESULT hr;
	// COMの初期化
	hr = CoInitializeEx( NULL, COINIT_MULTITHREADED );
	if( FAILED( hr ) )
	{	//COMの初期化に失敗
		return( FALSE );
	}
	return( TRUE );
}
//-------------------------------------
//COM終了処理
//引数：	無
//戻値：	BOOL			…	成功時TRUE、エラー時FALSE
//-------------------------------------
BOOL	UnInitCOM( void )
{
	// COMを閉じる
	CoUninitialize();
	return( TRUE );
}
